    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        pegawai = new EntPegawai();
        pegawai.setKode(txtKode.getText());
        pegawai.setNama(txtNama.getText());
        pegawai.setPassword(txtPassword.getText());
        pegawai.setTelp(txtTlp.getText());
        pegawai.setAlamat(txtAlamat.getText());
        
        if(process.equalsIgnoreCase("add")){
            condition =pegawaiDao.addPegawai(pegawai);
        }else if (process.equalsIgnoreCase("edit")){
            condition = pegawaiDao.editPegawai(pegawai);
        }
        
        if(condition==false){
            message = "Maaf Penyimpanan data gagal";
            JOptionPane.showMessageDialog(null, message,"Information",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

