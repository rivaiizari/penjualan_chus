private void refreshTable(){
    ListPegawai = pegawaiDao.selectPegawai(txtCari.getText(), txtCari.getText());
    dtm = (DefaultTableModel) TblPegawai.getModel();
    dtm.setRowCount(0);
    
    for (EntPegawai data : ListPegawai){
        dtm.addRow(new Object[]{data.getKode(),
            data.getNama(),
            data.getPassword(),
            data.getTelp(),
            data.getAlamat()});
    }
    
    if(TblPegawai.getRowCount() > 0){
        row = TblPegawai.getRowCount() - 1;
        TblPegawai.setRowSelectionInterval(row, row);
        aturButton(true);
        aturText(false);
        process = "";
    }
 }

